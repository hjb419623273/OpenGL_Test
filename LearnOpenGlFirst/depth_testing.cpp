/*
深度缓冲 depth buffer  深度值depth value
深度缓冲就像颜色缓冲一样 在每个片段中储存了信息，并且和颜色缓冲有着一样的宽度和高度。深度缓冲是由窗口系统自动创建的，它会以 16 24 32位float的
形式储存它的深度值 深度缓冲精度都是24位的
当深度测试被启用的时候 opengl会将一个片段的深度值与深度缓冲的内容进行对比。opengl会执行一个深度测试 如果这个测试通过的话 深度缓冲将会更新为新的
深度值 如果深度测试失败了 片段将会被丢弃

深度缓冲在片段着色器运行之后 在屏幕空间中运行的。屏幕空间坐标与通过OpenGL的viewport所定义的视口密切相关。并且可以直接使用GLSL内建立变量gl_FragCoord
从片段着色器中直接访问。 gl_FragCoord的x和y分量代表了片段的屏幕空间坐标 （0 0位于左下角）
gl_FragCoord中也包含了一个z分量 它包含了片段真正的深度值 z值就是需要与深度缓冲内容所对比的那个值

提前深度测试 允许深度测试在片段着色器之前运行。

如果一个片段通过了深度测试的话 opengl会在深度缓冲中储存该片段的z值 如果没有通过深度缓冲 则会丢弃该片段
启用了深度缓冲在每个渲染迭代之前使用GL_DEPTH_BUFFER_BIT来清除深度缓冲  否则会使用上次渲染迭代写入的深度值

深度值精度
深度缓冲包含了一个介于0和1之间的深度值。 将会与观察者视角所看到的场景中所有物体的z值进行比较 观察空间的z
值可能是投影平截体的近平面near和远平面 far直接的任何值 需要一种方式将这些观察空间z值变换到[0,1]范围之内

深度冲突
两个平面或者三角形紧密地平行排列在一起时会发生 深度缓冲没有足够的精度来决定两个形状哪个在前面
解决深度缓冲
1.不要把多个物体摆得太靠近 
2.尽可能将近平面设置远一些
3.牺牲一些性能 使用更高精度的深度缓冲 一般24位 大部分显卡支持32位深度缓冲
*/

void framebuffer_size_callback(GLFWwindow* window, int width, int height);
void mouse_callback(GLFWwindow* window, double xpos, double ypos);
void scroll_callback(GLFWwindow* window, double xoffset, double yoffset);
